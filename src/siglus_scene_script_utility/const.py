def _sanitize_ident(s):
    s = str(s)
    out = []
    for ch in s:
        out.append(
            ch.upper() if ("0" <= ch <= "9" or "A" <= ch.upper() <= "Z") else "_"
        )
    t = "".join(out)
    while "__" in t:
        t = t.replace("__", "_")
    if t and t[0].isdigit():
        t = "_" + t
    return t


